    private boolean isValidEmail(String email) { 
    String regex = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
    if(!email.matches(regex)){
        JOptionPane.showMessageDialog(null, "Erreur! Format Email invalide","Mail",JOptionPane.ERROR_MESSAGE);
        return false;}
     return true;
    }
    
